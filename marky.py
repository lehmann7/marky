def run_inline(fpath, n, i, text):
	print("# `!`", fpath, "%d:%d" % (n, i), text)
	try:
		value = str(eval('f"""{%s}"""' % text, exec_dict, None))
	except Exception as ex:
		print("ERROR", type(ex), str(ex))
		exit(1)
	for a, b in enumerate(value.split("\n")):
		print("#    %03d" % (a + 1), b)
	return value

