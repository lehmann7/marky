def print_inline(fpath, n, i, text):
	print("# `!`", fpath, "%d:%d" % (n, i), "{%s}" % text, end="", flush=True)
	try:
		value = ("{" + text + "}").format(**exec_dict)
	except Exception as ex:
		print("")
		print("ERROR", type(ex), str(ex))
		exit(1)
	if "\n" in value:
		print("")
		for a, b in enumerate(value.split("\n")):
			print("#    %03d" % (a + 1), b)
	else:
		print(" ->", value)
	return value

